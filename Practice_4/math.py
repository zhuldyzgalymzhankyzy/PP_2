# math.py
import math
import random

# 1. Square root
def square_root(x):
    return math.sqrt(x)


# 2. Factorial
def factorial(n):
    return math.factorial(n)


# 3. Random number
def random_number(a, b):
    return random.randint(a, b)


# 4. GCD
def gcd(a, b):
    return math.gcd(a, b)